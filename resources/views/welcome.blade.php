      <p>
      </p>
      You are welcome to ‘A Beautiful life By Kenny’. My name is Kenny – Kehinde
      Nwani (nee Lokulo-Sodipe). I am first and foremost a child of God, married
      to a great guy called Andy Nwani, and we are blessed with 3 wonderful
      children aged 25, 17 and 13. Career wise I am an Educationist and a
      Business Executive, the CEO of Meadow Hall Group and also a Social
      Entrepreneur (Founder – Meadow Hall Foundation).
  </p>
  <br/>
  <p>
      You may have noticed how I described myself and all the positions I occupy
      – as God’s daughter, as a wife, as a mother, as a career woman and as a
      nation builder. If you look carefully, you will also see an order of
      priority.
  </p>
  <br/>
  <p>
      This order was informed by the account of my all-time role model, one of
      the greatest women that ever lived called Deborah (Her story is told in
      Judges 4 in the bible). Her life spells out clearly our 5 purposes as women
      according to an order of priority.
  </p><br/>
  <p>
      Deborah was first and foremost referred to as a prophetess of God, which
      speaks of her relationship with her Maker. A prophetess (a true one that
      is), hears from God. I believe this is where it all starts, giving God
      first place in our hearts. This indeed is what dictates what will happen in
      every other area of our lives and ultimately guarantees our success.
  </p><br/>
  <p>
      Then she was described as the wife of Lapidoth, which represents our second
      priority as women. No matter how great she was, she willingly put herself
      under the leadership of HER HUSBAND. What a great example we have in her as
      21<sup>st</sup> century modern women, especially in these days of feminism.
      Please let us learn from those who have gone ahead of us. “What if I’m not
      married”, some might ask? Jesus is your husband. Indeed Paul appealed to
      people to be like him, fulfilled by simply satisfying his Lord. But we
      thank God for Jesus who gave us the institution of marriage. So you don’t
      have a husband yet? It’s perfectly okay! No P! Continue serving your
      heavenly Lord until your ‘earthly lord’ comes along. He makes all things
      beautiful in his own time. The Lord will grant you the desire of your heart
      in Jesus name, Amen.
  </p><br/>
  <p>
      Deborah was a wife and a Mother as we weren’t told she didn’t have
      children, indeed she was called a Mother-in-Israel. I believe this is
      instructive. Being a mother goes beyond mothering your biological children.
      As Deborah was a mother to many others, a mother to the nation, so should
      we! So if you are in the ‘waiting room’, I declare that the God who
      answered me and gave me a new song after eight years of secondary
      infertility, blessing me with 2 more children will answer you in Jesus
      name, Amen. Nevertheless you are already a mother. A mother to the numerous
      ‘spiritual’ children out there (younger ones who look up to you in church,
      at work, in your community). Yes we are all mothers in Israel (or wherever
      Israel represents to you, for me it is Nigeria).
  </p><br/>
  <p>
      Deborah was called a Judge. This was her career which earned her a lot of
      respect nationwide, so shall we be recognized for the impact we make in our
      different careers, businesses and sphere of influence in Jesus name, Amen.
      We shall be impactful and be solution providers in our own field, and the
      Lord’s name will be glorified in our lives, in Jesus name, Amen. I believe
      each of us (like Joseph) has a field where people will bow before us as
      Joseph’s siblings and parents bowed in recognition of God’s anointing (key
      words are ‘’God’s anointing’’) upon us in that area of our calling.
      <br/>
      If you are the CEO of your home, please realize that you are still
      fulfilling purpose. A lady called Jael in the same scripture in Judges 4
      was a home maker, who did great and mighty things for her country and
      fulfilled purpose even as a homemaker.
  </p><br/>
  <p>
      Lastly, Deborah went to war on behalf of her nation. She was a Nation
      builder. She went beyond her home and career and made an impact on her
      nation and generation. My favourite saying is, “You can build a nest (your
      home) and still fly (spread your wings and touch the world)”. Charity
      begins at home but doesn’t end there. Cater to the needs of your family
      first by all means, and spread your tentacles and affect other lives beyond
      your family.
  </p><br/>
  <p>
      Great lessons from an awesome woman, you’ll agree with me. Like I said, she
      is my role model as she lived a life of purpose. I am extremely purpose
      driven and the older I get the more important it becomes to me to fulfill
      purpose. Sisters, there is no time to waste on things that don’t matter but
      rather let us focus on fulfilling purpose. A life without purpose is a
      vain, shallow and empty one and no matter what we try to fill it with, we
      will remain unfulfilled and unhappy. On the other hand, a life of purpose
      is so exciting! Never a boring moment! I am never bored, too busy
      fulfilling purpose.
  </p><br/>
  <p>
      My Final Thoughts;
      <br/>
      Deborah had a ministry, she was a prophetess. She SERVED God – not for
      money, fame, recognition or ambition. She SERVED Him in her capacity as a
      PROPHETESS. What is your ministry? What is (or are) your area(s) of
      personal service to God? Please note, this is different from your
      relationship with God. YOU ARE SAVED TO SERVE. He has called me to be a
      MENTOR to ladies (and Children actually), a Ministry I take very seriously.
      <br/>
      Titus 2: 3 – 5 is a ministry call for me, as I see the need for this
      ministry all around me – there is a need for us to encourage ourselves as
      women first of all, to a deeper walk with God to get to the stage where we
      are led by Him and His Spirit in all things and all ways, where His will
      becomes our will. A place where the secular and spiritual merge and there
      is no significant difference.
  </p><br/>
  <p>
      Let’s come together, to build up ourselves, our daughters, sisters and
      sisters-in-law, nannies, indeed all women. Please join me my sisters, let
      us assist each other to enjoy the fullness of joy the bible promises. If
      this kind of Joy were unattainable, the Lord would not say so – for God is
      not a man that he should lie. He said ask that your Joy may be full - John
      16:24.
      <br/>
      I LOVE YOU ALL!!!!!
  </p>

